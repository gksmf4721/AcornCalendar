DROP PROCEDURE IF EXISTS PROC_UPDATE_VACT_CNT;

DELIMITER //
CREATE PROCEDURE PROC_UPDATE_VACT_CNT(
  IN P_SEQ BIGINT,
  IN P_JOIN_COMP_DT DATE,
  IN P_type VARCHAR(20)
)
BEGIN
  UPDATE JH_MEMBER
  SET M_JOIN_COMP_DT = P_JOIN_COMP_DT,
      M_VACT_CNT = CASE
        WHEN P_JOIN_COMP_DT IS NULL THEN COALESCE(M_VACT_CNT, 0)
        ELSE 15 + GREATEST(TIMESTAMPDIFF(YEAR, P_JOIN_COMP_DT, CURDATE()), 0)
      END
  WHERE M_SEQ = P_SEQ;
END//
DELIMITER ;
